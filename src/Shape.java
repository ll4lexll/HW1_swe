public enum Shape {
    /* enum for cards shape */
    Spades(){
        public String toString() {
            return "♠";
        }
    },
    Diamonds(){
        public String toString() {
            return "♦";
        }
    },
    Clubs(){
        public String toString() {
            return "♣";
        }
    },
    Hearts(){
        public String toString() {
            return "♥";
        }
    }

}
